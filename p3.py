import turtle
def moraba():
    for i in range(4):
        turtle.forward(100)
        turtle.right(90)
    turtle.forward(20)
turtle.pencolor("blue")
moraba()
turtle.pencolor("red")
moraba()
turtle.pencolor("orange")
moraba()
turtle.pencolor("green")
moraba()
turtle.pencolor("purple")
moraba()
turtle.pencolor("yellow")
moraba()
turtle.done()